#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	char	*ptr;
	int		i;

	len = 0;
	while (s[len])
	{
		len++;
	}
	ptr = malloc(sizeof(char) * (len + 1));
	if (!ptr)
		return (NULL);
	i = 0;
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
